library(ISLR)
summary(Carseats)
attach(Carseats)
table(Urban)
table(US)
ls.fit<-lm(Sales~Price+Urban+US,data=Carseats)
summary(ls.fit)
ls.fit1<-lm(Sales~Price+US)
summary(ls.fit1)
anova(ls.fit,ls.fit1)
confint(ls.fit1,level=0.95)
par(mfrow=c(2,2))
plot(ls.fit1)
plot(ls.fit1$fitted.values,rstudent(ls.fit1))
